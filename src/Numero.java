public class Numero {
    // Declaramos la variable que almacena el valor
    private double valor;

    // Constructor
    public Numero(double valor) {
        this.valor = valor;
    }

    // Método que devuelve el valor almacenado en la variable
    public double getValor() {
        return valor;
    }

    // Método que permite modificar el valor almacenado en la variable
    public void setValor(double valor) {
        this.valor = valor;
    }
}
